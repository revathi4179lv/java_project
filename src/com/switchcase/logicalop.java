package com.switchcase;

public class logicalop {
	public static void main(String[] args) {
		System.out.println();
		int i=5;
		char c='s';
		if (i==5 && c=='S') {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
		System.out.println();
		if (i==5 || c=='S') {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
		System.out.println();
	
		
	}

}
