package com.switchcase;

public class increop {
	
public static void main(String[] args) {
	int i=5;
	System.out.println(i);
	System.out.println(i++);
	System.out.println(i);
	System.out.println(++i);
	System.out.println(i++);
	
	
	System.out.println();
	System.out.println();
	int a=3;
	System.out.println(a);
	System.out.println(a--);
	System.out.println(a);
	System.out.println(--a);
	System.out.println(a);
	System.out.println(--a);
}

}
