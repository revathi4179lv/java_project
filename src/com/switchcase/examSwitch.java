package com.switchcase;

public class examSwitch {
	public static void main(String[] args) {
		char c='c';
		switch (c) {
		case 'a':
			System.out.println("first letter");
			break;
		case 'b':
			System.out.println("second letter");
			break;
		case 'c':
	System.out.println("third letter");
			break;
		case 'd':
			System.out.println("fourth Letter");
			break;
		case 'e':
			System.out.println("fifth letter");
			break;

		default:
			System.out.println("it IS a alphabet letter");
			break;
		}
		
		
		}
	}


